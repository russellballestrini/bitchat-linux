static size_t tlv_append(uint8_t *out, size_t off, uint8_t type,
                         const uint8_t *value, size_t len) {
    out[off++] = type;
    out[off++] = (uint8_t)len;
    memcpy(out + off, value, len);
    return off + len;
}

size_t fx_build_announce_tlv(const char *nickname, uint8_t *out, size_t cap) {
    return fx_build_announce_tlv_with_neighbors(nickname, NULL, 0, out, cap);
}

size_t fx_build_announce_tlv_with_neighbors(const char *nickname,
                                            const uint8_t *neighbors_8b,
                                            size_t neighbor_count,
                                            uint8_t *out, size_t cap) {
    size_t need = 2 + strlen(nickname) + 2 + 32 + 2 + 32
                + (neighbor_count > 0 ? 2 + neighbor_count * 8 : 0);
    if (need > cap) return 0;
    size_t off = 0;
    off = tlv_append(out, off, 0x01, (const uint8_t *)nickname, strlen(nickname));
    off = tlv_append(out, off, 0x02, FX_NOISE_PUBKEY,   sizeof(FX_NOISE_PUBKEY));
    off = tlv_append(out, off, 0x03, FX_SIGNING_PUBKEY, sizeof(FX_SIGNING_PUBKEY));
    if (neighbor_count > 0 && neighbors_8b) {
        off = tlv_append(out, off, 0x04, neighbors_8b, neighbor_count * 8);
    }
    return off;
}
